public class Calculator {
    public static float sum(float a, float b) {
        return a + b;
    }

    public static float diff(float a, float b) {
        return a - b;
    }

    public static float inmultire(float a, float b) {
        return a * b;
    }

    public static float impartire(float a, float b) {
        return a / b;
    }
}
